
public class PlayerAttributes {
	int skating;
	int shooting;
	int hands;
	int passing;
	int defence;
	int physical;
	
	public PlayerAttributes(int skating, int shooting, int hands, int passing, int defence, int physical) {
		this.skating = skating;
		this.shooting = shooting;
		this.hands = hands;
		this.passing = passing;
		this.defence = defence;
		this.physical = physical;
	}
}
