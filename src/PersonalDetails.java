
public class PersonalDetails {
	String firstname;
	String lastname;
	String birthCountry;
	String birthStateProvince;
	String city;
	int draftyear;
	int draftpos;
	int height;
	int weight;
	String handness;
	
	public PersonalDetails(String firstname, String lastname, String birthCountry, String birthStateProvince, String city, int draftyear, int draftpos, int height, int weight, String handness) {
		this.firstname = firstname;
		this.lastname = lastname;
		this.birthCountry = birthCountry;
		this.birthStateProvince = birthStateProvince;
		this.city = city;
		this.draftyear = draftyear;
		this.draftpos = draftpos;
		this.height = height;
		this.weight = weight;
		this.handness = handness;
	}
}
