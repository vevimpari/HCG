
public class Team {
	String name;
	String city;
	String abbreviation;
	String division;
	String conference;
	
	public Team(String name, String city, String abbreviation, String division, String conference)  {
		this.name = name;
		this.city = city;
		this.abbreviation = abbreviation;
		this.division = division;
		this.conference = conference;
	}
}